/* ════════════════════════════════════════════════
   PARINAMA — ExportPanel Component
   Export/download panel for JSON, text, and markdown
   ════════════════════════════════════════════════ */

import { useState, useCallback } from 'react';
import { motion, AnimatePresence } from 'framer-motion';

/* ── Export Format Config ────────────────────── */

const EXPORT_FORMATS = [
  {
    id: 'json',
    label: 'JSON',
    icon: '{ }',
    description: 'Full session data with scores and metadata',
    extension: '.json',
    mimeType: 'application/json',
  },
  {
    id: 'text',
    label: 'Text',
    icon: 'Aa',
    description: 'Plain text with original and evolved prompts',
    extension: '.txt',
    mimeType: 'text/plain',
  },
  {
    id: 'markdown',
    label: 'Markdown',
    icon: 'M↓',
    description: 'Formatted markdown report with tables',
    extension: '.md',
    mimeType: 'text/markdown',
  },
];

/* ── Format Generators ───────────────────────── */

function generateJSON(data) {
  const exportData = {
    parinama_version: '1.0.0',
    exported_at: new Date().toISOString(),
    session: {
      id: data.sessionId,
      original_prompt: data.originalPrompt,
      evolved_prompt: data.evolvedPrompt,
      total_generations: data.totalGenerations,
      best_generation: data.bestGeneration,
    },
    scores: {
      original: data.originalScores,
      final: data.finalScores,
      improvement: calculateImprovement(data.originalScores, data.finalScores),
    },
    generations: data.generations?.map((gen, i) => ({
      generation: i,
      prompt: gen.prompt,
      scores: gen.scores,
      overall_score: gen.overallScore,
      mutation_type: gen.mutationType || null,
      mutation_reason: gen.mutationReason || null,
    })) || [],
    metadata: {
      providers_used: data.providersUsed || [],
      llm_switches: data.llmSwitches || 0,
    },
  };

  return JSON.stringify(exportData, null, 2);
}

function generateText(data) {
  const divider = '═'.repeat(50);
  const subDivider = '─'.repeat(50);
  const improvement = calculateImprovement(data.originalScores, data.finalScores);

  let text = '';
  text += `${divider}\n`;
  text += `PARINAMA — Evolution Report\n`;
  text += `${divider}\n\n`;

  text += `Date: ${new Date().toLocaleDateString()}\n`;
  text += `Session: ${data.sessionId || 'N/A'}\n`;
  text += `Generations: ${data.totalGenerations}\n\n`;

  text += `${subDivider}\n`;
  text += `ORIGINAL PROMPT\n`;
  text += `${subDivider}\n\n`;
  text += `${data.originalPrompt}\n\n`;

  text += `Score: ${calculateOverall(data.originalScores)}/100\n`;
  text += `  Clarity:       ${data.originalScores?.clarity ?? 0}\n`;
  text += `  Specificity:   ${data.originalScores?.specificity ?? 0}\n`;
  text += `  Actionability: ${data.originalScores?.actionability ?? 0}\n`;
  text += `  Conciseness:   ${data.originalScores?.conciseness ?? 0}\n`;
  text += `  Creativity:    ${data.originalScores?.creativity ?? 0}\n\n`;

  text += `${subDivider}\n`;
  text += `EVOLVED PROMPT\n`;
  text += `${subDivider}\n\n`;
  text += `${data.evolvedPrompt}\n\n`;

  text += `Score: ${calculateOverall(data.finalScores)}/100\n`;
  text += `  Clarity:       ${data.finalScores?.clarity ?? 0}\n`;
  text += `  Specificity:   ${data.finalScores?.specificity ?? 0}\n`;
  text += `  Actionability: ${data.finalScores?.actionability ?? 0}\n`;
  text += `  Conciseness:   ${data.finalScores?.conciseness ?? 0}\n`;
  text += `  Creativity:    ${data.finalScores?.creativity ?? 0}\n\n`;

  text += `${subDivider}\n`;
  text += `IMPROVEMENT\n`;
  text += `${subDivider}\n\n`;
  text += `Overall: ${improvement.overall > 0 ? '+' : ''}${improvement.overall}\n`;
  Object.entries(improvement.dimensions).forEach(([key, val]) => {
    text += `  ${key}: ${val > 0 ? '+' : ''}${val}\n`;
  });

  text += `\n${divider}\n`;
  text += `Generated by PARINAMA — prompt evolution engine\n`;
  text += `${divider}\n`;

  return text;
}

function generateMarkdown(data) {
  const improvement = calculateImprovement(data.originalScores, data.finalScores);

  let md = '';
  md += `# PARINAMA — Evolution Report\n\n`;
  md += `> *परिणाम — transformation through evolution*\n\n`;
  md += `**Date:** ${new Date().toLocaleDateString()}  \n`;
  md += `**Session:** \`${data.sessionId || 'N/A'}\`  \n`;
  md += `**Generations:** ${data.totalGenerations}  \n\n`;

  md += `---\n\n`;

  md += `## Original Prompt\n\n`;
  md += `\`\`\`\n${data.originalPrompt}\n\`\`\`\n\n`;

  md += `## Evolved Prompt\n\n`;
  md += `\`\`\`\n${data.evolvedPrompt}\n\`\`\`\n\n`;

  md += `---\n\n`;

  md += `## Score Comparison\n\n`;
  md += `| Dimension | Original | Final | Change |\n`;
  md += `|-----------|----------|-------|--------|\n`;

  const dims = ['clarity', 'specificity', 'actionability', 'conciseness', 'creativity'];
  dims.forEach(dim => {
    const orig = data.originalScores?.[dim] ?? 0;
    const final = data.finalScores?.[dim] ?? 0;
    const delta = final - orig;
    const deltaStr = delta > 0 ? `+${delta}` : `${delta}`;
    const emoji = delta > 0 ? '🟢' : delta < 0 ? '🔴' : '⚪';
    md += `| ${dim.charAt(0).toUpperCase() + dim.slice(1)} | ${orig} | ${final} | ${emoji} ${deltaStr} |\n`;
  });

  md += `| **Overall** | **${calculateOverall(data.originalScores)}** | **${calculateOverall(data.finalScores)}** | **${improvement.overall > 0 ? '+' : ''}${improvement.overall}** |\n\n`;

  if (data.generations && data.generations.length > 1) {
    md += `## Evolution Path\n\n`;
    data.generations.forEach((gen, i) => {
      if (i === 0) {
        md += `1. **Original** — Score: ${gen.overallScore ?? calculateOverall(gen.scores)}\n`;
      } else {
        md += `${i + 1}. **Gen ${i}** [${gen.mutationType || 'mutated'}] — Score: ${gen.overallScore ?? calculateOverall(gen.scores)}\n`;
      }
    });
    md += `\n`;
  }

  md += `---\n\n`;
  md += `*Generated by [PARINAMA](https://github.com/parinama) — prompt evolution engine*\n`;

  return md;
}

/* ── Helpers ──────────────────────────────────── */

function calculateOverall(scores) {
  if (!scores) return 0;
  const weights = { clarity: 0.25, specificity: 0.20, actionability: 0.20, conciseness: 0.20, creativity: 0.15 };
  let total = 0;
  Object.entries(weights).forEach(([key, weight]) => {
    total += (scores[key] ?? 0) * weight;
  });
  return Math.round(total);
}

function calculateImprovement(originalScores, finalScores) {
  const dims = ['clarity', 'specificity', 'actionability', 'conciseness', 'creativity'];
  const dimensions = {};
  dims.forEach(dim => {
    dimensions[dim] = (finalScores?.[dim] ?? 0) - (originalScores?.[dim] ?? 0);
  });

  return {
    overall: calculateOverall(finalScores) - calculateOverall(originalScores),
    dimensions,
  };
}

/* ── Download Trigger ────────────────────────── */

function downloadFile(content, filename, mimeType) {
  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
}

/* ── Format Card ─────────────────────────────── */

function FormatCard({ format, onExport, isExporting, exportedFormat }) {
  const isExported = exportedFormat === format.id;

  return (
    <motion.button
      onClick={() => onExport(format.id)}
      disabled={isExporting}
      whileHover={{ borderColor: 'var(--accent-amber)', y: -2 }}
      whileTap={{ scale: 0.98 }}
      style={{
        display: 'flex',
        flexDirection: 'column',
        alignItems: 'center',
        gap: '0.5rem',
        padding: '1.25rem 1rem',
        borderRadius: '6px',
        border: `1px solid ${isExported ? 'var(--accent-green)' : 'var(--border)'}`,
        backgroundColor: isExported ? 'rgba(34, 197, 94, 0.04)' : 'var(--bg-surface)',
        cursor: isExporting ? 'wait' : 'pointer',
        transition: 'all 200ms ease',
        width: '100%',
        textAlign: 'center',
      }}
    >
      {/* Icon */}
      <motion.span
        animate={isExported ? { scale: [1, 1.2, 1] } : {}}
        transition={{ duration: 0.3 }}
        style={{
          fontFamily: "'DM Mono', monospace",
          fontSize: '1.25rem',
          fontWeight: 600,
          color: isExported ? 'var(--accent-green)' : 'var(--accent-amber)',
          width: '40px',
          height: '40px',
          display: 'flex',
          alignItems: 'center',
          justifyContent: 'center',
          borderRadius: '8px',
          backgroundColor: isExported
            ? 'rgba(34, 197, 94, 0.10)'
            : 'rgba(217, 119, 6, 0.08)',
        }}
      >
        {isExported ? '✓' : format.icon}
      </motion.span>

      {/* Label */}
      <span
        style={{
          fontFamily: "'Inter', sans-serif",
          fontSize: '0.875rem',
          fontWeight: 600,
          color: 'var(--text-primary)',
        }}
      >
        {format.label}
      </span>

      {/* Description */}
      <span
        style={{
          fontFamily: "'Inter', sans-serif",
          fontSize: '0.6875rem',
          color: 'var(--text-muted)',
          lineHeight: 1.4,
        }}
      >
        {isExported ? 'Downloaded!' : format.description}
      </span>
    </motion.button>
  );
}

/* ══════════════════════════════════════════════
   MAIN — ExportPanel Component
   ══════════════════════════════════════════════ */

export default function ExportPanel({
  sessionId = null,
  originalPrompt = '',
  evolvedPrompt = '',
  originalScores = {},
  finalScores = {},
  totalGenerations = 0,
  bestGeneration = null,
  generations = [],
  providersUsed = [],
  llmSwitches = 0,
  className = '',
  style: externalStyle = {},
}) {
  const [isExporting, setIsExporting] = useState(false);
  const [exportedFormat, setExportedFormat] = useState(null);
  const [isOpen, setIsOpen] = useState(false);

  const exportData = {
    sessionId,
    originalPrompt,
    evolvedPrompt,
    originalScores,
    finalScores,
    totalGenerations,
    bestGeneration,
    generations,
    providersUsed,
    llmSwitches,
  };

  const handleExport = useCallback(async (formatId) => {
    setIsExporting(true);

    try {
      const format = EXPORT_FORMATS.find(f => f.id === formatId);
      if (!format) return;

      let content = '';
      switch (formatId) {
        case 'json':
          content = generateJSON(exportData);
          break;
        case 'text':
          content = generateText(exportData);
          break;
        case 'markdown':
          content = generateMarkdown(exportData);
          break;
        default:
          return;
      }

      const timestamp = new Date().toISOString().slice(0, 10);
      const filename = `parinama-evolution-${timestamp}${format.extension}`;

      downloadFile(content, filename, format.mimeType);
      setExportedFormat(formatId);

      /* Reset after 3 seconds */
      setTimeout(() => setExportedFormat(null), 3000);
    } catch (err) {
      console.error('Export failed:', err);
    } finally {
      setIsExporting(false);
    }
  }, [exportData]);

  /* Also try API export for JSON */
  const handleAPIExport = useCallback(async () => {
    if (!sessionId) return;

    try {
      const res = await fetch(`/api/sessions/${sessionId}/export`);
      if (res.ok) {
        const data = await res.json();
        const content = JSON.stringify(data, null, 2);
        const timestamp = new Date().toISOString().slice(0, 10);
        downloadFile(content, `parinama-${timestamp}.json`, 'application/json');
        setExportedFormat('json');
        setTimeout(() => setExportedFormat(null), 3000);
      }
    } catch {
      /* Fall back to client-side export */
      handleExport('json');
    }
  }, [sessionId, handleExport]);

  return (
    <div
      className={`export-panel ${className}`}
      style={externalStyle}
    >
      {/* Toggle button */}
      <motion.button
        onClick={() => setIsOpen(!isOpen)}
        whileHover={{ borderColor: 'var(--accent-amber)' }}
        whileTap={{ scale: 0.98 }}
        style={{
          display: 'flex',
          alignItems: 'center',
          justifyContent: 'center',
          gap: '0.375rem',
          width: '100%',
          padding: '0.5rem 0.75rem',
          borderRadius: '6px',
          border: '1px solid var(--border)',
          backgroundColor: 'var(--bg-surface)',
          color: 'var(--text-secondary)',
          fontFamily: "'Inter', sans-serif",
          fontSize: '0.8125rem',
          fontWeight: 500,
          cursor: 'pointer',
          transition: 'all 200ms ease',
        }}
      >
        <span style={{ fontSize: '0.875rem' }}>↓</span>
        Export Results
        <motion.span
          animate={{ rotate: isOpen ? 180 : 0 }}
          transition={{ duration: 0.2 }}
          style={{ fontSize: '0.625rem', marginLeft: '0.25rem' }}
        >
          ▾
        </motion.span>
      </motion.button>

      {/* Expandable panel */}
      <AnimatePresence>
        {isOpen && (
          <motion.div
            initial={{ height: 0, opacity: 0 }}
            animate={{ height: 'auto', opacity: 1 }}
            exit={{ height: 0, opacity: 0 }}
            transition={{ duration: 0.3, ease: [0.22, 1, 0.36, 1] }}
            style={{ overflow: 'hidden' }}
          >
            <div
              style={{
                display: 'grid',
                gridTemplateColumns: 'repeat(3, 1fr)',
                gap: '0.625rem',
                marginTop: '0.75rem',
              }}
            >
              {EXPORT_FORMATS.map((format) => (
                <FormatCard
                  key={format.id}
                  format={format}
                  onExport={format.id === 'json' && sessionId ? handleAPIExport : () => handleExport(format.id)}
                  isExporting={isExporting}
                  exportedFormat={exportedFormat}
                />
              ))}
            </div>
          </motion.div>
        )}
      </AnimatePresence>
    </div>
  );
}
